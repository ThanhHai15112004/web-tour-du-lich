// Hàm tạo card chuyến bay
function createFlightCard(flight) {
  return `
    <div class="flight-card">
        <div class="flight-info">
            <div class="route-logo-container">
                <div class="route">
                    <span class="city">${flight.route.from}</span>
                    <span class="arrow">${flight.route.arrow}</span>
                    <span class="city">${flight.route.to}</span>
                </div>
                <img src="${flight.airline_logo.src}" alt="${flight.airline_logo.alt}" class="airline-logo">
            </div>
            <div class="dates">
                <span>${flight.dates.start}</span> - <span>${flight.dates.end}</span>
            </div>
            <div class="price-btn-container">
                <div class="price">${flight.price}</div>
                <a href="${flight.view_link}" class="view-btn">Xem</a>
            </div>
        </div>
    </div>
    `;
}

// Hiển thị các chuyến bay trong nước
